package com.project.tmc.chart_models;

public class Fill {
    int color1 =0;
    int color2 = 0;

    public Fill(int color1, int color2) {
        this.color1 = color1;
        this.color2 = color2;
    }

    public int getColor1() {
        return color1;
    }

    public void setColor1(int color1) {
        this.color1 = color1;
    }

    public int getColor2() {
        return color2;
    }

    public void setColor2(int color2) {
        this.color2 = color2;
    }
}
